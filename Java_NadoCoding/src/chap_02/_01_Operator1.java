package chap_02;

public class _01_Operator1 {
    public static void main(String[] args) {
//        산술 연산자
//        일반 연산
        System.out.println(4 + 2);
        System.out.println(4 - 2);
        System.out.println(4 * 2);
        System.out.println(4 / 2);
        System.out.println(5 / 2); // 2
        System.out.println(2 / 4 ); // 0
        System.out.println(4 % 2);

        int val;
        val  = 10;
        System.out.println(val);
        System.out.println(++val);
        System.out.println(val++);
        System.out.println(val);

        ++val;
    }
}
