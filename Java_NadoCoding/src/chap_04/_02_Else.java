package chap_04;

public class _02_Else {
    public static void main(String[] args) {
        int hour = 13;
        if (hour < 14){
            System.out.println(hour + " 보다 작습니다.");
        }
        else {
            System.out.println(hour + " 보다 큽니다.");
        }
        System.out.println("출력 완료");
    }
}
